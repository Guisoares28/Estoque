    private void bt_save_cadastro_categoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_save_cadastro_categoriaActionPerformed
        
        String categoria = txt_categoria_cadastro.getText().trim();
        
        if(categoria.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nome não pode ser vazio",
                            "Falha", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        CategoriaModel categoriaModel = new CategoriaModel();
        categoriaModel.setCategoria(categoria);
        
        try {
            CategoriaDAO.adicionarCategoria(categoriaModel);
        } catch (SQLException ex) {
            Logger.getLogger(f_cadastro_categoria.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_bt_save_cadastro_categoriaActionPerformed

