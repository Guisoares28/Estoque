    private void txt_categoria_cadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_categoria_cadastroActionPerformed
        try {
            String categoria = CategoriaDAO.buscarCategoria(txt_categoria_cadastro.getText().trim());
            
            if(categoria.isEmpty()){
                txt_categoria_att.setText("");
               return; 
            }
            txt_categoria_att.setEnabled(true);
            txt_categoria_att.setText(categoria.toUpperCase());
            txt_categoria_cadastro.setEditable(false);
            
            
        } catch (SQLException ex) {
            Logger.getLogger(f_cadastro_categoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txt_categoria_cadastroActionPerformed

    private void bt_atualizar_categoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_atualizar_categoriaActionPerformed
        String novo_nome_categoria = txt_categoria_att.getText().trim();
        String antigo_nome = txt_categoria_cadastro.getText().trim();
        if(!novo_nome_categoria.isEmpty()){
            if(!novo_nome_categoria.equalsIgnoreCase(txt_categoria_cadastro.getText())){
            try {
                CategoriaDAO.atualizarCategoria(antigo_nome ,novo_nome_categoria);
                txt_categoria_cadastro.setText(novo_nome_categoria);
                txt_categoria_cadastro.setEditable(true);
                txt_categoria_att.setEnabled(false);
            } catch (SQLException ex) {
                Logger.getLogger(f_cadastro_categoria.class.getName()).log(Level.SEVERE, null, ex);
            }
            }else{
              JOptionPane.showMessageDialog(null, "O nome é o mesmo, para atualizar coloque um novo nome",
                            "Falha", JOptionPane.INFORMATION_MESSAGE);
            }   
        }else{
            JOptionPane.showMessageDialog(null, "O novo nome não pode ser vazio!",
                            "Falha", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
        
        
       
        
    }//GEN-LAST:event_bt_atualizar_categoriaActionPerformed

