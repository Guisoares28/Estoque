    private void bt_atualizar_fornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_atualizar_fornecedorActionPerformed
        String cnpj = txt_cnpj_fornecedor.getText().trim();
        String nome = txt_nome_fornecedor.getText().trim();
        String telefone = txt_telefone_fornecedor.getText().trim();
        
        if(nome.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nome não pode ser vazio!",
                            "Aviso", JOptionPane.INFORMATION_MESSAGE); 
            return;
        }
        
        if(telefone.isEmpty()){
            JOptionPane.showMessageDialog(null, "Telefone não pode ser vazio!",
                            "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
        
        try {
            FornecedorDAO.atualizarFornecedor(cnpj, nome, telefone);
            txt_cnpj_fornecedor.setText("");
            txt_cnpj_fornecedor.setEditable(true);
            txt_nome_fornecedor.setText("");
            txt_telefone_fornecedor.setText("");
            txt_nome_fornecedor.setEnabled(false);
            txt_telefone_fornecedor.setEnabled(false);
            bt_atualizar_fornecedor.setVisible(false);
            bt_cancelar.setVisible(false);
            jButton2.setEnabled(true);
        } catch (SQLException ex) {
            Logger.getLogger(f_cadastrar_fornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_bt_atualizar_fornecedorActionPerformed

