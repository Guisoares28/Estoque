    private void txt_cnpj_fornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_cnpj_fornecedorActionPerformed
        
        String cnpj = txt_cnpj_fornecedor.getText().trim();
        
        try {
            FornecedorModel fornecedor = FornecedorDAO.buscarFornecedorPorCnpj(cnpj);
            
            if(fornecedor.getId() == null){
                txt_nome_fornecedor.setEnabled(true);
                txt_telefone_fornecedor.setEnabled(true);
                jButton2.setEnabled(true);
            }else{
                txt_cnpj_fornecedor.setEditable(false);
                txt_nome_fornecedor.setEnabled(true);
                txt_telefone_fornecedor.setEnabled(true);
                txt_nome_fornecedor.setText(fornecedor.getNome());
                txt_telefone_fornecedor.setText(fornecedor.getTelefone());
                bt_atualizar_fornecedor.setVisible(true);
                bt_cancelar.setVisible(true);
                jButton2.setEnabled(false);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(f_cadastrar_fornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txt_cnpj_fornecedorActionPerformed

