    private void bt_buscar_fornecedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_buscar_fornecedoresActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) table_buscar_fornecedores.getModel();
        modelo.setRowCount(0);
        
        String nome = txt_nome_buscar_func.getText();
        
        List<FornecedorModel> fornecedores;
                
        if(nome.isEmpty()){
            try {
                fornecedores = FornecedorDAO.buscarTodosFornecedores();
                fornecedores.forEach(f -> modelo.addRow(new Object[]{
                    f.getId(),
                    f.getNome(),
                    f.getTelefone(),
                    f.getCnpj()
                }));
            } catch (SQLException ex) {
                Logger.getLogger(f_listar_forncedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            try{
                fornecedores = FornecedorDAO.buscarFornecedorPorContainNome(nome);
                fornecedores.forEach(f -> modelo.addRow(new Object[]{
                    f.getId(),
                    f.getNome(),
                    f.getTelefone(),
                    f.getCnpj()
                }));
            } catch (SQLException ex) {
                Logger.getLogger(f_listar_forncedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
    }//GEN-LAST:event_bt_buscar_fornecedoresActionPerformed

