    private void table_buscar_fornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_buscar_fornecedoresMouseClicked
        if (evt.getClickCount() == 2) {
        int linha = table_buscar_fornecedores.getSelectedRow();
        if (linha != -1) {
            // Pega o ID (assumindo que est√° na primeira coluna)
            int idFornecedor = (int) table_buscar_fornecedores.getValueAt(linha, 0);
            
            
            try {
                // Chama a outra tela, passando o ID
                f_deletar_fornecedor deletarForne = new f_deletar_fornecedor(idFornecedor);
                deletarForne.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(f_listar_forncedores.class.getName()).log(Level.SEVERE, null, ex);
            }
            // Fecha essa tela, se desejar
            this.dispose(); 
        }
    }
    }//GEN-LAST:event_table_buscar_fornecedoresMouseClicked

